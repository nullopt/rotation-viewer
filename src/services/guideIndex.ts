export function getGuideCategories(): string[] {
    const categories = new Set(GUIDE_INDEX.map((e) => e.category));
    return [...categories].sort((a, b) =>
        a.localeCompare(b, undefined, { sensitivity: "base" })
    );
}

export function getGuidesByCategory(): Map<string, GuideEntry[]> {
    const unsorted = new Map<string, GuideEntry[]>();

    for (const entry of GUIDE_INDEX) {
        const list = unsorted.get(entry.category);
        if (list) {
            list.push(entry);
        } else {
            unsorted.set(entry.category, [entry]);
        }
    }

    const sortedCategories = [...unsorted.keys()].sort((a, b) =>
        a.localeCompare(b, undefined, { sensitivity: "base" })
    );

    const sorted = new Map<string, GuideEntry[]>();
    for (const category of sortedCategories) {
        const entries = unsorted.get(category)!;
        entries.sort((a, b) =>
            a.label.localeCompare(b.label, undefined, { sensitivity: "base" })
        );
        sorted.set(category, entries);
    }

    return sorted;
}
